#include "ft_printf.h"

int	ft_printf_str(char *s)
{
	int	i;

	if (!s)
		s = "(null)";
	i = 0;
	while (s[i])
	{
		ft_printf_char(s[i]);
		i++;
	}
	return (i);
}
